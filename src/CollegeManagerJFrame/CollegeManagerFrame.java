    private void ViewAllStudentorProfessor(DefaultTableModel JTable) {

        try {

            CollegeManager cm = new CollegeManager();
            cm.ViewStudent();

            ResultSet rs = cm.ViewStudent();
            JTable.setRowCount(0);

            while (rs.next()) {
                String SNum = rs.getString("NUMBER");
                String SName = rs.getString("NAME");
                String SMajor = rs.getString("MAJOR");

                JTable.insertRow(JTable.getRowCount(), new Object[]{SNum, SName, SMajor});
            }

            rs = cm.ViewProfessor();
            while (rs.next()) {
                String PNum = rs.getString("NUMBER");
                String PName = rs.getString("NAME");
                String PMajor = rs.getString("MAJOR");

                JTable.insertRow(JTable.getRowCount(), new Object[]{PNum, PName, PMajor});
            }
        } catch (SQLException e) {
            JOptionPane.showInternalMessageDialog(null, "학생 출력 오류");
        }
    }
