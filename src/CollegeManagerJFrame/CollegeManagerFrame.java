    private void SearchButtMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_SearchButtMouseClicked
        // TODO add your handling code here:

        SearchInfo s = new SearchInfo();
        String target = SearchField.getText();
        ResultSet rs = null;
        if (!SearchStudent.isSelected() && !SearchProfessor.isSelected()) {
            JOptionPane.showInternalMessageDialog(null, "PLEASE SELECT STUDENT OR PROFESSOR");
            return;
        }
        try {
            model.setRowCount(0);
            if (target.matches("^[SP]\\d{3}$") || target.matches("^[가-힣]*$")) {
                if (SearchStudent.isSelected()) {
                    rs = s.StudentSearch(target);
                } else {
                    rs = s.ProffesorSearch(target);
                }
            }

            while (rs.next()) {
                String name = rs.getString("NAME");
                String number = rs.getString("NUMBER");
                String major = rs.getString("MAJOR");
                model.insertRow(model.getRowCount(), new Object[]{number, name, major});
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "ERROR !");
        }
    }//GEN-LAST:event_SearchButtMouseClicked

